/*Kreirati switch strukturu prema želji sa više caseova i default opcijom. Ispisati pripadne rezultate korištenjem console.log(). */

let x = prompt("Unesite broj: ");
let num = parseInt(x);

switch (num) {
  case 1:
    console.log("Uneseni broj je 1.");
    break;

  case 2:
    console.log("Uneseni broj je 2.");
    break;

  case 3:
    console.log("Uneseni broj je 3.");
    break;
}
